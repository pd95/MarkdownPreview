import { createHash } from "node:crypto";
import { execFileSync, spawnSync } from "node:child_process";
import {
    copyFileSync,
    existsSync,
    mkdtempSync,
    mkdirSync,
    readFileSync,
    readdirSync,
    renameSync,
    rmSync,
    writeFileSync
} from "node:fs";
import { dirname, join, relative, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const toolsDirectory = dirname(fileURLToPath(import.meta.url));
const repositoryRoot = resolve(toolsDirectory, "..");
const resourcesDirectory = join(
    repositoryRoot,
    "MarkdownPipeline",
    "Sources",
    "MarkdownPipeline",
    "Resources",
    "WebResources"
);
const swiftMetadataPath = join(
    repositoryRoot,
    "MarkdownPipeline",
    "Sources",
    "MarkdownPipeline",
    "WebDependencyMetadata.generated.swift"
);
const inventoryPath = join(repositoryRoot, "WEB_DEPENDENCIES.json");
const noticesPath = join(repositoryRoot, "THIRD_PARTY_NOTICES");

const dependencies = [
    {
        argument: "highlightjs",
        displayName: "highlight.js",
        packageName: "@highlightjs/cdn-assets",
        source: "https://github.com/highlightjs/highlight.js",
        expectedLicense: "BSD-3-Clause"
    },
    {
        argument: "katex",
        displayName: "KaTeX",
        packageName: "katex",
        source: "https://github.com/KaTeX/KaTeX",
        expectedLicense: "MIT"
    },
    {
        argument: "mermaid",
        displayName: "Mermaid",
        packageName: "mermaid",
        source: "https://github.com/mermaid-js/mermaid",
        expectedLicense: "MIT"
    }
];

const fixedManagedResourceNames = new Set([
    "highlight.min.js",
    "katex.min.css",
    "katex.min.js",
    "KaTeX-LICENSE.txt",
    "mermaid.min.js",
    "stackoverflow-dark.min.css",
    "stackoverflow-light.min.css"
]);

function packageDirectory(installationDirectory, packageName) {
    return join(installationDirectory, "node_modules", ...packageName.split("/"));
}

function readJSON(path) {
    return JSON.parse(readFileSync(path, "utf8"));
}

function dependencyMetadata(dependency, lockfile, installationDirectory) {
    const directory = packageDirectory(installationDirectory, dependency.packageName);
    const packageMetadataPath = join(directory, "package.json");
    if (!existsSync(packageMetadataPath)) {
        throw new Error(
            `Missing ${dependency.packageName}. Run npm --prefix Tools ci --ignore-scripts first.`
        );
    }

    const packageMetadata = readJSON(packageMetadataPath);
    const lockKey = `node_modules/${dependency.packageName}`;
    const lockedMetadata = lockfile.packages?.[lockKey];
    if (!lockedMetadata?.integrity) {
        throw new Error(`package-lock.json has no integrity entry for ${dependency.packageName}.`);
    }
    if (packageMetadata.version !== lockedMetadata.version) {
        throw new Error(
            `${dependency.packageName} is ${packageMetadata.version} in node_modules but `
                + `${lockedMetadata.version} in package-lock.json.`
        );
    }
    if (packageMetadata.license !== dependency.expectedLicense) {
        throw new Error(
            `${dependency.packageName} changed license from ${dependency.expectedLicense} `
                + `to ${packageMetadata.license ?? "an unspecified license"}. Review it manually.`
        );
    }

    return {
        ...dependency,
        directory,
        version: packageMetadata.version,
        license: packageMetadata.license,
        integrity: lockedMetadata.integrity,
        licenseText: readFileSync(join(directory, "LICENSE"), "utf8").trim()
    };
}

function sha256(contents) {
    return createHash("sha256").update(contents).digest("hex");
}

function relativePath(path) {
    return relative(repositoryRoot, path).split("\\").join("/");
}

function compareText(left, right) {
    return left < right ? -1 : left > right ? 1 : 0;
}

function generatedSwift(metadata, fontNames) {
    const quotedFonts = fontNames.map(name => `        "${name}",`).join("\n");
    const version = argument => metadata.find(item => item.argument === argument).version;
    return `// Generated by Tools/web-dependencies.mjs. Do not edit.\n\n`
        + `enum WebDependencyMetadata {\n`
        + `    static let highlightJSVersion = "${version("highlightjs")}"\n`
        + `    static let kaTeXVersion = "${version("katex")}"\n`
        + `    static let mermaidVersion = "${version("mermaid")}"\n\n`
        + `    static let kaTeXFontNames = [\n${quotedFonts}\n    ]\n`
        + `}\n`;
}

function generatedNotices(metadata) {
    const sections = metadata.map(item => `${item.displayName}\n`
        + `Version: ${item.version}\n`
        + `License: ${item.license}\n`
        + `Source: ${item.source}\n\n`
        + `${item.licenseText}`);
    sections.push(`KaTeX fonts
License: SIL Open Font License 1.1
Source: https://github.com/KaTeX/KaTeX

Copyright (c) 2009-2010 Design Science, Inc.
Copyright (c) 2014-2018 Khan Academy

The bundled KaTeX WOFF2 files are derived from the MathJax TeX fonts. The
complete SIL Open Font License 1.1 text is distributed with the font files as
KaTeX-FONTS-OFL.txt.`);

    return `Third-Party Notices\n\n`
        + `Generated by Tools/web-dependencies.mjs. Do not edit manually.\n\n`
        + `This project bundles the following third-party software:\n\n`
        + `${sections.join("\n\n")}\n`;
}

function buildOutputs(installationDirectory = toolsDirectory) {
    const packageJSON = readJSON(join(installationDirectory, "package.json"));
    const lockfile = readJSON(join(installationDirectory, "package-lock.json"));
    const metadata = dependencies.map(dependency =>
        dependencyMetadata(dependency, lockfile, installationDirectory)
    );
    for (const item of metadata) {
        const declaredVersion = packageJSON.devDependencies?.[item.packageName];
        if (declaredVersion !== item.version) {
            throw new Error(
                `${item.packageName} must be pinned exactly to ${item.version}; found `
                    + `${declaredVersion ?? "no pin"}.`
            );
        }
    }

    const outputs = new Map();
    const add = (path, contents) => {
        outputs.set(path, Buffer.isBuffer(contents) ? contents : Buffer.from(contents));
    };

    const highlight = metadata.find(item => item.argument === "highlightjs");
    const highlightCorePath = join(highlight.directory, "es", "core.min.js");
    const highlightCoreModule = readFileSync(highlightCorePath, "utf8");
    const defaultExport = /export\{([A-Za-z_$][\w$]*) as default\};?\s*$/;
    const exportMatch = defaultExport.exec(highlightCoreModule);
    if (!exportMatch) {
        throw new Error(
            "The highlight.js core module no longer has the expected default export. Review it manually."
        );
    }
    const highlightCore = highlightCoreModule.replace(defaultExport, `var hljs=${exportMatch[1]};`);
    const highlightLanguagesDirectory = join(highlight.directory, "languages");
    const highlightLanguageFiles = readdirSync(highlightLanguagesDirectory)
        .filter(name => name.endsWith(".min.js"))
        .sort();
    if (highlightLanguageFiles.length === 0) {
        throw new Error("The highlight.js CDN package contains no minified language modules.");
    }
    const highlightParts = [
        Buffer.from(`/*! Generated from @highlightjs/cdn-assets v${highlight.version}. Do not edit. */\n`),
        Buffer.from(highlightCore),
        Buffer.from("\n")
    ];
    for (const name of highlightLanguageFiles) {
        highlightParts.push(readFileSync(join(highlightLanguagesDirectory, name)), Buffer.from(";\n"));
    }
    add(join(resourcesDirectory, "highlight.min.js"), Buffer.concat(highlightParts));
    for (const theme of ["stackoverflow-dark.min.css", "stackoverflow-light.min.css"]) {
        add(join(resourcesDirectory, theme), readFileSync(join(highlight.directory, "styles", theme)));
    }

    const katex = metadata.find(item => item.argument === "katex");
    add(join(resourcesDirectory, "katex.min.js"), readFileSync(join(katex.directory, "dist", "katex.min.js")));
    add(join(resourcesDirectory, "katex.min.css"), readFileSync(join(katex.directory, "dist", "katex.min.css")));
    add(join(resourcesDirectory, "KaTeX-LICENSE.txt"), `${katex.licenseText}\n`);
    const katexFontsDirectory = join(katex.directory, "dist", "fonts");
    const fontNames = readdirSync(katexFontsDirectory)
        .filter(name => name.startsWith("KaTeX_") && name.endsWith(".woff2"))
        .sort();
    if (fontNames.length === 0) {
        throw new Error("The KaTeX package contains no WOFF2 fonts.");
    }
    for (const name of fontNames) {
        add(join(resourcesDirectory, name), readFileSync(join(katexFontsDirectory, name)));
    }

    const mermaid = metadata.find(item => item.argument === "mermaid");
    add(
        join(resourcesDirectory, "mermaid.min.js"),
        readFileSync(join(mermaid.directory, "dist", "mermaid.min.js"))
    );

    add(swiftMetadataPath, generatedSwift(metadata, fontNames));
    add(noticesPath, generatedNotices(metadata));

    const artifacts = [...outputs.entries()]
        .map(([path, contents]) => ({
            path: relativePath(path),
            sha256: sha256(contents)
        }))
        .sort((left, right) => compareText(left.path, right.path));
    const inventory = {
        schemaVersion: 1,
        dependencies: metadata.map(item => ({
            name: item.displayName,
            package: item.packageName,
            version: item.version,
            license: item.license,
            source: item.source,
            integrity: item.integrity
        })),
        artifacts
    };
    add(inventoryPath, `${JSON.stringify(inventory, null, 2)}\n`);

    return outputs;
}

function isManagedResourceName(name) {
    return fixedManagedResourceNames.has(name)
        || (name.startsWith("KaTeX_") && name.endsWith(".woff2"));
}

function unexpectedManagedPaths(outputs) {
    return readdirSync(resourcesDirectory)
        .filter(isManagedResourceName)
        .map(name => join(resourcesDirectory, name))
        .filter(path => !outputs.has(path));
}

function atomicWrite(path, contents) {
    mkdirSync(dirname(path), { recursive: true });
    const temporaryPath = `${path}.tmp-${process.pid}`;
    try {
        writeFileSync(temporaryPath, contents);
        renameSync(temporaryPath, path);
    } finally {
        rmSync(temporaryPath, { force: true });
    }
}

function applyOutputs(outputs) {
    for (const [path, contents] of outputs) {
        atomicWrite(path, contents);
        console.log(`generated ${relativePath(path)}`);
    }
    for (const stalePath of unexpectedManagedPaths(outputs)) {
        rmSync(stalePath);
        console.log(`removed ${relativePath(stalePath)}`);
    }
}

function generate() {
    applyOutputs(buildOutputs());
}

function verify() {
    const outputs = buildOutputs();
    const failures = [];
    for (const stalePath of unexpectedManagedPaths(outputs)) {
        failures.push(`unexpected managed file: ${relativePath(stalePath)}`);
    }
    for (const [path, expected] of outputs) {
        if (!existsSync(path)) {
            failures.push(`missing: ${relativePath(path)}`);
            continue;
        }
        const actual = readFileSync(path);
        if (!actual.equals(expected)) {
            failures.push(`out of date: ${relativePath(path)}`);
        }
    }
    if (failures.length > 0) {
        throw new Error(`Generated web dependencies do not match:\n- ${failures.join("\n- ")}`);
    }
    console.log(`Verified ${outputs.size} generated files.`);
}

function npmViewVersion(packageName) {
    return execFileSync("npm", ["view", packageName, "version", "--json"], {
        cwd: toolsDirectory,
        encoding: "utf8"
    }).trim().replace(/^"|"$/g, "");
}

function isExactSemanticVersion(version) {
    return /^(0|[1-9]\d*)\.(0|[1-9]\d*)\.(0|[1-9]\d*)(?:-[0-9A-Za-z-]+(?:\.[0-9A-Za-z-]+)*)?(?:\+[0-9A-Za-z-]+(?:\.[0-9A-Za-z-]+)*)?$/.test(version);
}

function currentPins() {
    const packageJSON = readJSON(join(toolsDirectory, "package.json"));
    return new Map(dependencies.map(item => [item.argument, packageJSON.devDependencies[item.packageName]]));
}

function check() {
    const pins = currentPins();
    const latest = new Map();
    console.log("Web dependency versions:\n");
    console.log("Dependency              Current        Latest         Status");
    for (const item of dependencies) {
        const current = pins.get(item.argument);
        const available = npmViewVersion(item.packageName);
        if (!isExactSemanticVersion(available)) {
            throw new Error(`npm returned a non-semantic latest version for ${item.packageName}: ${available}`);
        }
        latest.set(item.argument, available);
        const status = current === available ? "current" : "update available";
        console.log(
            `${item.displayName.padEnd(23)} ${current.padEnd(14)} ${available.padEnd(14)} ${status}`
        );
    }
    return latest;
}

function managedGitPaths() {
    const generatedPaths = existsSync(inventoryPath)
        ? readJSON(inventoryPath).artifacts.map(artifact => artifact.path)
        : [];
    return [...new Set([
        "Tools/package.json",
        "Tools/package-lock.json",
        "WEB_DEPENDENCIES.json",
        ...generatedPaths
    ])];
}

function assertManagedFilesAreClean() {
    const result = execFileSync("git", ["status", "--porcelain", "--", ...managedGitPaths()], {
        cwd: repositoryRoot,
        encoding: "utf8"
    }).trim();
    if (result) {
        throw new Error(
            "Commit or stash existing web-dependency changes before updating:\n" + result
        );
    }
}

function stageUpdate(specifications) {
    mkdirSync(join(repositoryRoot, "tmp"), { recursive: true });
    const stagedDirectory = mkdtempSync(join(repositoryRoot, "tmp", "web-dependency-update-"));
    try {
        for (const name of [".npmrc", "package.json", "package-lock.json"]) {
            copyFileSync(join(toolsDirectory, name), join(stagedDirectory, name));
        }

        const installation = spawnSync(
            "npm",
            ["install", "--save-dev", "--save-exact", "--ignore-scripts", ...specifications],
            { cwd: stagedDirectory, stdio: "inherit" }
        );
        if (installation.status !== 0) {
            throw new Error("npm failed to stage the web dependency update.");
        }

        const audit = spawnSync("npm", ["audit", "--audit-level=high"], {
            cwd: stagedDirectory,
            stdio: "inherit"
        });
        if (audit.status !== 0) {
            throw new Error("npm reported a high or critical dependency vulnerability.");
        }

        const outputs = buildOutputs(stagedDirectory);
        atomicWrite(
            join(toolsDirectory, "package.json"),
            readFileSync(join(stagedDirectory, "package.json"))
        );
        atomicWrite(
            join(toolsDirectory, "package-lock.json"),
            readFileSync(join(stagedDirectory, "package-lock.json"))
        );
        applyOutputs(outputs);

        rmSync(join(toolsDirectory, "node_modules"), { recursive: true, force: true });
        renameSync(join(stagedDirectory, "node_modules"), join(toolsDirectory, "node_modules"));
        verify();
    } finally {
        rmSync(stagedDirectory, { recursive: true, force: true });
    }
}

function update(arguments_) {
    const explicitVersions = new Map();
    let acceptLatest = false;
    for (const argument of arguments_) {
        if (argument === "--accept-latest") {
            acceptLatest = true;
            continue;
        }
        const match = /^--(highlightjs|katex|mermaid)=(.+)$/.exec(argument);
        if (!match) {
            throw new Error(`Unknown update argument: ${argument}`);
        }
        if (!isExactSemanticVersion(match[2])) {
            throw new Error(
                `Invalid version for --${match[1]}: ${match[2]}. Use an exact semantic version such as 1.2.3.`
            );
        }
        explicitVersions.set(match[1], match[2]);
    }
    if (acceptLatest && explicitVersions.size > 0) {
        throw new Error("Use either --accept-latest or explicit dependency versions, not both.");
    }

    const latest = check();
    if (!acceptLatest && explicitVersions.size === 0) {
        console.log("\nNo files changed. To update all dependencies to the versions above, run:");
        console.log("npm --prefix Tools run web:update -- --accept-latest");
        return;
    }

    assertManagedFilesAreClean();
    const pins = currentPins();
    const specifications = dependencies.map(item => {
        const version = explicitVersions.get(item.argument)
            ?? (acceptLatest ? latest.get(item.argument) : pins.get(item.argument));
        return `${item.packageName}@${version}`;
    });
    stageUpdate(specifications);
    console.log("\nUpdate generated and verified. Review the diff and run npm --prefix Tools run web:test.");
}

function usage() {
    console.error("Usage: node Tools/web-dependencies.mjs <check|generate|update|verify>");
    process.exitCode = 64;
}

try {
    const [command, ...arguments_] = process.argv.slice(2);
    switch (command) {
    case "check":
        check();
        break;
    case "generate":
        generate();
        break;
    case "update":
        update(arguments_);
        break;
    case "verify":
        verify();
        break;
    default:
        usage();
    }
} catch (error) {
    console.error(error instanceof Error ? error.message : error);
    process.exitCode = 1;
}
